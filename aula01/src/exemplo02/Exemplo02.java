package exemplo02;

public class Exemplo02 {
    public static void main(String[] args) {
        // estrutura condicional
        int numero = 0;

        if (numero > 0) {
            System.out.println("O valor é positivo.");
        } else if (numero < 0){
            System.out.println("O valor é negativo.");
        }
        else {
            System.out.println("O valor é zero.");
        }
    }
}
